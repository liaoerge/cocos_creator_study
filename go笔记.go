// 进程/线程/协程
// 进程是独立的，进程A崩溃不会影响到进程B
// 线程是寄生在进程上的，线程如果有崩溃会带着进程一起崩，开多个线程会有性能开销
// 协程是寄生在线程上的，正常出错只影响一个任务，只影响到他的当次，协程可以开N个，不会有性能开销
// 类比一个场景：
// 进程：独立车间，一个炸了不影响别的
// 线程：车间里的工人，一个疯了整个车间报废
// 协程：工人手里的小工单，极多、极轻，正常错了不影响，但严重崩溃也会炸车间

