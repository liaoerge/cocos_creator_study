// 一致性哈希
// 一致性哈希的四大特点
// 1. 单调性机器增减，数据只会迁移到相邻节点，不会乱跳。
// 2. 平衡性数据尽量均匀分布（靠虚拟节点实现）。
// 3. 分散性相同 key 一定路由到同一台机器。
// 4. 最小迁移机器变化，只影响少量数据

// 最形象的比喻（必记）
// - 哈希环 = 圆形跑道
// - 机器 = 站在跑道上的人
// - 数据 = 飞盘
// 规则：飞盘落在哪里，就交给顺时针最近的人。
// - 来一个新人 → 只抢他前面一段的飞盘
// - 走一个人 → 他的飞盘交给下一个人
// - 远处的飞盘完全不受影响
// 这就是一致性。

// 最终超级总结（你记这个就够）
// 一致性哈希就是：把机器和数据都放在一个环上，数据顺时针找最近机器。机器增减时，只动附近一小部分数据，保证大部分缓存不失效。
